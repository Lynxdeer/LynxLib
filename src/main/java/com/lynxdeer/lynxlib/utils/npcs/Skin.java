package com.lynxdeer.lynxlib.utils.npcs;

public class Skin {
	
	public String head;
	
	public String bodyTop;
	public String bodyBottom;
	
	public String leftArmTop;
	public String leftArmBottom;
	
	public String rightArmTop;
	public String rightArmBottom;
	
	public Skin(String singleTexture) {
		head = singleTexture;
		bodyTop = singleTexture;
		bodyBottom = singleTexture;
		leftArmTop = singleTexture;
		leftArmBottom = singleTexture;
		rightArmTop = singleTexture;
		rightArmBottom = singleTexture;
	}
	
}
